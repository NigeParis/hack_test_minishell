#include <stddef.h>

size_t	ft_pow(size_t x, size_t y)
{
	size_t	res;

	res = 1;
	if (y == 0)
		return (1);
	if (x == 0)
		return (0);
	while (y > 0)
	{
		res *= x;
		y--;
	}
	return (res);
}
