#include "ft_math.h"

int	test_ft_abs(void)
{
	int	x;

	x = -42;
	if (ft_abs(x) != 42)
		return (1);
	x = 42;
	if (ft_abs(x) != 42)
		return (1);
	x = 0;
	if (ft_abs(x) != 0)
		return (1);
	return (0);
}
