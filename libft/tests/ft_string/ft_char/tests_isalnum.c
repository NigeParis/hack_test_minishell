#include "ft_char.h"

int	test_ft_isalnum(void)
{
	int	i;

	i = 0;
	while (i < 256)
	{
		if (ft_isalnum(i) != ((i >= '0' && i <= '9') || (i >= 'a' && i <= 'z')
				|| (i >= 'A' && i <= 'Z')))
			return (1);
		i++;
	}
	return (0);
}
