#include "ft_char.h"

int	test_ft_isascii(void)
{
	int	i;

	i = -1;
	while (i < 256)
	{
		if (ft_isascii(i) != (i >= 0 && i <= 127))
			return (1);
		i++;
	}
	return (0);
}
