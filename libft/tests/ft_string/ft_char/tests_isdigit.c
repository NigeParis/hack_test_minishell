#include "ft_char.h"

int	test_ft_isdigit(void)
{
	int	i;

	i = 0;
	while (i < 256)
	{
		if (ft_isdigit(i) != (i >= '0' && i <= '9'))
			return (1);
		i++;
	}
	return (0);
}
