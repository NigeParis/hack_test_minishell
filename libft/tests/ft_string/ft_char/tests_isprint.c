#include "ft_char.h"

int	test_ft_isprint(void)
{
	int	i;

	i = 0;
	while (i < 256)
	{
		if (ft_isprint(i) != (i >= 32 && i <= 126))
			return (1);
		i++;
	}
	return (0);
}
